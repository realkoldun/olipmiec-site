/**
 * Получить новости с пагинацией
 */
export function getNews({
  page = 1,
  limit = 6,
  category,
  tag,
}: {
  page?: number;
  limit?: number;
  category?: string;
  tag?: string;
}): { items: NewsItem[]; total: number; totalPages: number } {
  let filtered = mockNews.filter((news) => news.published);

  if (category) {
    filtered = filtered.filter((news) => news.category === category);
  }

  if (tag) {
    filtered = filtered.filter((news) => news.tags.includes(tag));
  }

  const total = filtered.length;
  const totalPages = Math.ceil(total / limit);
  const start = (page - 1) * limit;
  const items = filtered.slice(start, start + limit);

  return { items, total, totalPages };
}

/**
 * Получить новость по ID
 */
export function getNewsById(id: string): NewsItem | undefined {
  return mockNews.find((news) => news.id === id);
}
